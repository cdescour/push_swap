#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int		i;
	int		lens;
	char	*str;

	if (s == NULL || f == NULL)
		return (NULL);
	i = 0;
	lens = ft_strlen(s);
	str = malloc(sizeof(char) * lens + 1);
	if (!str)
		return (NULL);
	while (s[i])
	{
		str[i] = (*f)(i, s[i]);
		i++;
	}
	str[i] = 0;
	return (str);
}
